
const ShowProfilePictureForm = () => {

    const ProfilePictureFormContainer = document.querySelector('.ProfilePictureFormContainer');
    const UpdateableBtnContainer = document.querySelector('.UpdateableBtnContainer');

    UpdateableBtnContainer.style.display = 'none'
    ProfilePictureFormContainer.style.display = 'block'

}


const HideProfilePictureForm = () => {

    const ProfilePictureFormContainer = document.querySelector('.ProfilePictureFormContainer');
    const UpdateableBtnContainer = document.querySelector('.UpdateableBtnContainer');

    UpdateableBtnContainer.style.display = 'block'
    ProfilePictureFormContainer.style.display = 'none'

}



